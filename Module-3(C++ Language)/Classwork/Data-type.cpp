//Data types program in C++
#include<iostream>
using namespace std;

int main()
{
	//Integer Data type
	int age = 25;
	
	//Float datatype
	float height = 5.7f;
	double salary = 65000.54;
	
	//character data-type
	char grade = 'A';
	
	//boolean data-type
	bool isstudent = true;
	
	//string datatype
	string name = "Yash";
	
	//long-integer
	long population = 780000000;
	
	//short-integer
	short year = 2026;
	
	/*Display the output*/
	cout<<"Integer Value = "<<age<<endl;
	cout<<"Float Value of Height = "<<height<<endl;
	cout<<"Double salary = "<<salary<<endl;
	cout<<"Character of Grade"<<grade<<endl;
	cout<<"Boolean of student = "<<isstudent<<endl;
	cout<<"Name of the student = "<<name<<endl;
	cout<<"Population of World = "<<population<<endl;
	cout<<"Year = "<<year<<endl;
	
	return 0;
}